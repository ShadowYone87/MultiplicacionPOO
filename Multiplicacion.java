// Se declara la clase
public class Multiplicacion {
    // Se declaran las variables
    int a, b, r;

    // Se declara el metodo
    public int multiplicar(int a, int b) {
        // Se llama al objeto
        r = this.a * this.b;
        return r;
        // Se cierra el metodo multiplicar
    }
    // Se cierra la clase
}
